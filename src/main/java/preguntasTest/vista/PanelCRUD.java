    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if (editar) {
            editarInsertarRespuestas();
            DAOProyecto.getInstance().editarPregunta(pregunta, tbPregunta.getText().toString());
            
            JOptionPane.showMessageDialog(null, "Pregunta editada correctamente.");
            cargarLista();

            btnRegistrar.setText("Registrar");
            editar = false;
        }
        else {
            if (tbPregunta.getText().equals("") || tbOp1.getText().equals("") || tbOp2.getText().equals("") || tbOp3.getText().equals("") || tbOp4.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Debes introducir todos los valores para poder registrar una pregunta.");
            }
            else {
                pregunta = new Pregunta((DAOProyecto.getInstance().obtenerIDPregMax() + 1), tbPregunta.getText().toString(), usuario.getId());
                DAOProyecto.getInstance().insertarPregunta(pregunta);
                editarInsertarRespuestas();
                
                cargarLista();
            }
        }
        
        this.updateUI();
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        if (jListaPreguntas.getSelectedIndices().length == 0) {
            JOptionPane.showMessageDialog(null, "Debes seleccionar mínimo un usuario para poder eliminarlo.");
        }
        else {
            int[] filas = jListaPreguntas.getSelectedIndices();
            
            for (int i = 0; i < filas.length; i++) {
                DAOProyecto.getInstance().eliminarRespuestas(DAOProyecto.getInstance().obtenerRespuestas(listaPreguntas.get(filas[i])));
                DAOProyecto.getInstance().eliminarPregunta(listaPreguntas.get(filas[i]));  
            }
                      
            cargarLista();
        }
        
        this.updateUI();
    }//GEN-LAST:event_btnBorrarActionPerformed

    private void btnLeerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLeerActionPerformed
        if (!editar) {
            if (jListaPreguntas.getSelectedIndices().length == 1) {
                pregunta = listaPreguntas.get(jListaPreguntas.getSelectedIndex());
                
                tbPregunta.setText(pregunta.getPregunta());
                rellenarRespuestas();

                btnRegistrar.setText("Editar");
                btnLeer.setText("Cancelar");
                editar = true;
            }
            else if (jListaPreguntas.getSelectedIndices().length == 0) {
                JOptionPane.showMessageDialog(null, "Debes seleccionar mínimo un usuario para poder editarlo.");
            }
            else {
                JOptionPane.showMessageDialog(null, "No puedes editar más de un usuario a la vez.");
            }
        }
        else {
            editar = false;
            
            btnRegistrar.setText("Registrar");
            btnLeer.setText("Leer");
        }
    }//GEN-LAST:event_btnLeerActionPerformed

