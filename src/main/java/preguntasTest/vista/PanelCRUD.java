    public void cargarLista() {
        listaPreguntas = DAOProyecto.getInstance().obtenerPreguntas(usuario);

        DefaultListModel modelo = new DefaultListModel();

        for (Pregunta pregunta : listaPreguntas) {
            modelo.addElement(pregunta.getPregunta());
        }

        jListaPreguntas.setModel(modelo);
    }
    
    private void editarInsertarRespuestas() {
        Enumeration<AbstractButton> buttons = rbtnGroup.getElements();
        String texto = "";

        for(int i = 0; i < rbtnGroup.getButtonCount(); i++){
            switch (i) {
                case 0:
                    texto = tbOp1.getText().toString();
                    break;
                case 1:
                    texto = tbOp2.getText().toString();
                    break;
                case 2:
                    texto = tbOp3.getText().toString();
                    break;
                default:
                    texto = tbOp4.getText().toString();
                    break;
            }

            AbstractButton actual = buttons.nextElement();
            if (editar) {
                DAOProyecto.getInstance().editarRespuesta(opciones.get(i), texto, actual.isSelected());
            }
            else {
                DAOProyecto.getInstance().insertarOpcion(new Opcion((DAOProyecto.getInstance().obtenerIDOpMax() + 1), pregunta.getId(), actual.isSelected(), texto));
            }
        }
    }
    
    public void rellenarRespuestas() {
        opciones = DAOProyecto.getInstance().obtenerRespuestas(pregunta);

        tbOp1.setText(opciones.get(0).getRespuesta());
        tbOp2.setText(opciones.get(1).getRespuesta());
        tbOp3.setText(opciones.get(2).getRespuesta());
        tbOp4.setText(opciones.get(3).getRespuesta());
        
        if (opciones.get(0).getCorrecta()) rbtnOp1.setSelected(true);
        else if (opciones.get(1).getCorrecta()) rbtnOp2.setSelected(true);
        else if (opciones.get(2).getCorrecta()) rbtnOp3.setSelected(true);
        else rbtnOp4.setSelected(true);
    }
    
